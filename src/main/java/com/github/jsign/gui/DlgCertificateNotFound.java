	private void doClose(int retStatus) {
        
		this.returnStatus = retStatus;
		
		if (this.certificateFinder != null) {
			this.certificateFinder.stop();
		}
		
        setVisible(false);
        
        dispose();        
    }

	public int getReturnStatus() {
		return returnStatus;
	}
	
	public void start() {
		
		this.tokensDriversInstalledOnSystem = jSign.getManager().getConfigurationManager().getTokensDriversInstalledOnSystem(jSign.getConfiguration());

		this.lblDriversInstallationHelpPage.setText(this.urlDriversInstallationHelpPage.toString());

		certificateFinder = new CertificateFinder() {
			
			@Override
			public void run() {
				
				while(!isStop()) {
					
					List<MSCAPIKeyStoreHelper> keyStoresHelpersAvailable = jSign.getManager().getConfigurationManager().getKeyStoresHelpersAvailableOnMsCapi();
					
					if (keyStoresHelpersAvailable.size() > 0) {
						doClose(RET_CERTIFICATE_FOUND);
					}
					else {
						try {
							Thread.sleep(1000);
						}
						catch (InterruptedException e) {
							e.printStackTrace();
						}
					}
				}
			}
		};
		
		threadCertificateFinder = new Thread(certificateFinder);
		threadCertificateFinder.start();
		
		pack();
		
        JFrameUtils.setCenterLocation(this);
        setVisible(true);   
	}
	
	private void init() {	
		
		this.lblDriversInstallationHelpPage.setCursor(Cursor.getPredefinedCursor(Cursor.HAND_CURSOR));
		this.lblDriversInstallationHelpPage.addMouseListener(new MouseListener() {

			@Override
			public void mouseReleased(MouseEvent e) {}

			@Override
			public void mousePressed(MouseEvent e) {
				Desktop desktop = Desktop.getDesktop();
				try {
					desktop.browse(urlDriversInstallationHelpPage.toURI());
				} 
				catch (Exception ex) {}
			}

			@Override
			public void mouseExited(MouseEvent e) {}

			@Override
			public void mouseEntered(MouseEvent e) {}

			@Override
			public void mouseClicked(MouseEvent e) {}
		});
		
		new EntityColumnWidthTableModel<Token>(tblTokensDrivers) {

			private static final long serialVersionUID = 1L;

			@Override
			public String[] getColumns() {
				return new String[] { "Nome", "Descrição" };
			}

			@Override
			public List<Token> getEntities() {
				return (List<Token>) tokensDriversInstalledOnSystem;
			}

			@Override
			public Object getValueAt(Token entity, int columnIndex) {
                switch (columnIndex) {
                    case 0 :
                        return entity.getName();
					case 1 :
                        return entity.getDescription();
                    default :
                        return null;
                }
			}

			@Override
			public Integer[] getColumnsWidth() {
				return new Integer[]{ 200, 350 };
			}
		};
	}
	
	public void setUrlDriversInstallationHelpPage(URL urlDriversInstallationHelpPage) {
		this.urlDriversInstallationHelpPage = urlDriversInstallationHelpPage;		
	}
